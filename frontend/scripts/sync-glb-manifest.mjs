import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const GLB_DIR = join(ROOT, 'public', 'models')
const META_PATH = join(GLB_DIR, 'models.meta.json')
const MANIFEST_PATH = join(ROOT, 'src', 'data', 'models.ts')

function slugify(name) {
  return name
    .toLowerCase()
    .replace(/\.glb$/i, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '') || 'model'
}

function humanize(stem) {
  return stem.replace(/[-_]+/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase())
}

function loadMeta() {
  if (!existsSync(META_PATH)) return {}
  return JSON.parse(readFileSync(META_PATH, 'utf8'))
}

function buildManifest() {
  const meta = loadMeta()
  const files = readdirSync(GLB_DIR)
    .filter((f) => f.toLowerCase().endsWith('.glb'))
    .sort()

  if (files.length === 0) {
    console.warn('No .glb files in public/models/')
  }

  return files.map((file) => {
    const stem = file.replace(/\.glb$/i, '')
    const override = meta[file] ?? {}
    return {
      id: override.id ?? slugify(stem),
      name: override.name ?? humanize(stem),
      description: override.description ?? `3D-модель «${humanize(stem)}» (.glb).`,
      glb: `/models/${file}`,
    }
  })
}

const models = buildManifest()

const content = `// Auto-generated by scripts/sync-glb-manifest.mjs — do not edit by hand.
// Add .glb files to public/models/ and run: npm run sync:models

export interface GlbModel {
  id: string
  name: string
  description: string
  glb: string
}

export const models: GlbModel[] = ${JSON.stringify(models, null, 2)}
`

writeFileSync(MANIFEST_PATH, content, 'utf8')
console.log(`Synced ${models.length} .glb model(s) → src/data/models.ts`)
